fun main(){
    // A variable is a container that stores data/values.
    // In kotlin we have 2 main types of initializers i.e var and val
    // Val => immutable(unchangable) var => mutable(changable)

    val name = "Ryan Mutuku" // string -> a series of characters inside a quote
    val number1 = 5 // int -> whole numbers
    val number2 = 3.14 // double -> decimal numbers
    var letter = "A" // char -> a single letter of the alphabet
    var isRegistered = true // boolean -> true or false

    println(name)
    println(number1)
    println(number2)
    println(letter)
    println(isRegistered)

}